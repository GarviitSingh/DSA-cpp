#include <iostream>
using namespace std;

int main() {
    // Integer types
    int x = 10;
    long y = 54;
    long long z = 154141455555;

    // Decimal types
    float a = 6.9f;
    double b = 69.9688;

    // Character type
    char ch = 'g';

    cout << "int: " << x << endl;
    cout << "long: " << y << endl;
    cout << "long long: " << z << endl;
    cout << "float: " << a << endl;
    cout << "double: " << b << endl;
    cout << "char: " << ch << endl;

    return 0;
}
